// Helper function to filter vehicles
export function filterVehicles(
  vehicles: Vehicle[],
  filters: {
    category?: string
    awd?: string
    ski_rack?: string
    min_rate?: string
    max_rate?: string
  }
): Vehicle[] {
  let filtered = [...vehicles]

  if (filters.category) {
    filtered = filtered.filter((v) => v.category === filters.category)
  }
  if (filters.awd === 'true') {
    filtered = filtered.filter((v) => v.is_awd)
  }
  if (filters.ski_rack === 'true') {
    filtered = filtered.filter((v) => v.has_ski_rack)
  }
  if (filters.min_rate) {
    filtered = filtered.filter((v) => v.daily_rate >= parseFloat(filters.min_rate!))
  }
  if (filters.max_rate) {
    filtered = filtered.filter((v) => v.daily_rate <= parseFloat(filters.max_rate!))
  }

  return filtered.sort((a, b) => a.daily_rate - b.daily_rate)
}
